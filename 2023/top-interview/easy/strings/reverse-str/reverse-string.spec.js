const reverseString = require("./reverse-string");
describe("", () => {
  it("should return ", () => {
    expect(reverseString(["h", "e", "l", "l", "o"])).toEqual([
      "o",
      "l",
      "l",
      "e",
      "h",
    ]);
  });

  it("should return ", () => {
    expect(reverseString(["m", "a", "m", "a"])).toEqual(["a", "m", "a", "m"]);
  });
});
