const reverseInt = require("./reverse-integer");
describe("", () => {
  it("should return ", () => {
    expect(reverseInt(123)).toBe(321);
  });

  it("should return ", () => {
    expect(reverseInt(-123)).toBe(-321);
  });

  it("should return ", () => {
    expect(reverseInt(1534236469)).toBe(0);
  });

  it("should return ", () => {
    expect(reverseInt(-2147483648)).toBe(0);
  });
});
