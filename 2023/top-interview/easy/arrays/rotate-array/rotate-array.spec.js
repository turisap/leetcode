const rotateArray = require("./rotate-array");
describe("", () => {
  it("should return ", () => {
    expect(rotateArray([1, 2, 3, 4, 5, 6, 7], 3)).toEqual([
      5, 6, 7, 1, 2, 3, 4,
    ]);
  });

  it("should return ", () => {
    expect(rotateArray([1, 2, 3, 4, 5, 6, 7], 3)).toEqual([
      5, 6, 7, 1, 2, 3, 4,
    ]);
  });

  it("should return ", () => {
    expect(rotateArray([1, 2], 3)).toEqual([2, 1]);
  });
});
