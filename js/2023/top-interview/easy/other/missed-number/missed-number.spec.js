const missedNumber = require("./missed-number");
describe("", () => {
  it("should return ", () => {
    expect(missedNumber([3, 0, 1])).toBe(2);
  });

  it("should return ", () => {
    expect(missedNumber([0, 1])).toBe(2);
  });

  it("should return ", () => {
    expect(missedNumber([9, 6, 4, 2, 3, 5, 7, 0, 1])).toBe(8);
  });

  it("should return ", () => {
    expect(missedNumber([1])).toBe(0);
  });

  it("should return ", () => {
    expect(missedNumber([0])).toBe(1);
  });
});
