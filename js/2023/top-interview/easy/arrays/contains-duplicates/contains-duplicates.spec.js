const containsDuplicates = require("./contains-duplicates");
describe("", () => {
  it("should return ", () => {
    expect(containsDuplicates([1, 2, 3, 4, 5])).toBe(false);
  });

  it("should return ", () => {
    expect(containsDuplicates([1, 2, 3, 2, 5])).toBe(true);
  });

  it("should return ", () => {
    expect(containsDuplicates([1, 2, 3, 1])).toBe(true);
  });
});
