const strStr = require("./string-search");
describe("", () => {
  it("should return ", () => {
    expect(strStr("sadbutsad", "sad")).toBe(0);
  });

  it("should return ", () => {
    expect(strStr("leetcode", "leeto")).toBe(-1);
  });

  it("should return ", () => {
    expect(strStr("mississippi", "issip")).toBe(4);
  });
});
